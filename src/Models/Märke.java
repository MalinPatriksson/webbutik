package Models;

public class Märke {
    int id;
    String märkesnamn;

    public Märke() {}

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getMärkesnamn() {
        return märkesnamn;
    }
    public void setMärkesnamn(String märkesnamn) {
        this.märkesnamn = märkesnamn;
    }
}
